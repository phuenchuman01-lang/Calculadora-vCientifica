public class CalculadoraBasica {
    // Ejemplo de método solicitado: Potencia
    public double calcularPotencia(double base, double exponente) {
        return Math.pow(base, exponente);
    }

    // Ejemplo de método solicitado: Mayor de dos números
    public double obtenerMayor(double n1, double n2) {
        return (n1 > n2) ? n1 : n2;
    }
}
